    public static boolean iniciar(String usuario, String contra) {
        AdminJugador aj = new AdminJugador("FiveNights.fnaf");
        aj.cargarArchivo();
        for (jugador object : aj.getJ()) {
            if (object.getUsuario().equals(usuario) && object.getContrasena().equals(contra)) {
                return true;
            }
        }
        return false;
    }

