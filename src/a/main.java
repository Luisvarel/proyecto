    private void c_listoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_c_listoActionPerformed
        if (c_usuario1 != null && c_contrasena != null) {
            if (nombre_unico(c_usuario1.getText())) {
                AdminJugador aj = new AdminJugador("./FiveNights.fnaf");
                jugador nuevo = new jugador(c_usuario1.getText(), c_contrasena.getText());
                aj.getJ().add(nuevo);
                aj.escribirArchivo();
                c_usuario1.setText("");
                c_contrasena.setText("");
                menu.setVisible(true);
                crear.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Intente con otro nombre");
            }
        }
    }//GEN-LAST:event_c_listoActionPerformed

    private void i_listoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_i_listoActionPerformed
        if (i_usuario.getText().equals("admin") && i_contrasena.getText().equals("admin")) {
            iniciar.setVisible(false);
            admin.setVisible(true);
            i_usuario.setText("");
            i_contrasena.setText("");
        } else if (iniciar(i_usuario.getText(), i_contrasena.getText())) {
            iniciar.setVisible(false);
            menu.setVisible(true);
            i_usuario.setText("");
            i_contrasena.setText("");
        }
    }//GEN-LAST:event_i_listoActionPerformed

